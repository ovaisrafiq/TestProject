    public function saveComment($post_id,$user_id,$comment)
    {
        $newComments = new Comments();
        $newComments
            ->setPost($post_id)
            ->setUser($user_id)
            ->setComment($comment)
            ->setCreatedAt(new \DateTime('now'));
            $this->_em->persist($newComments);
            $this->_em->flush();
    }

    



